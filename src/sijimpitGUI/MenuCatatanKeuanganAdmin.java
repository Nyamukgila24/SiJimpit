    private void tampilkanData(int bulan) {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("nama");
        model.addColumn("nik");
        model.addColumn("no_hp");
        model.addColumn("nominal");
        model.addColumn("tanggal");

        try (Connection conn = sijimpit.Koneksi.getConnection(); PreparedStatement pst = conn.prepareStatement("SELECT nama, nik, no_hp, nominal,  tanggal FROM menu_pembayaran_warga WHERE MONTH(tanggal) = ? ORDER BY tanggal ASC")) {

            pst.setInt(1, bulan);
            try (ResultSet rs = pst.executeQuery()) {

                while (rs.next()) {
                    java.sql.Date sqlDate = rs.getDate("tanggal");
                    String formattedDate = "";
                    if (sqlDate != null) {
                        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
                        formattedDate = sdf.format(new Date(sqlDate.getTime()));
                    }
                    model.addRow(new Object[]{
                        rs.getString("nama"),
                        rs.getString("nik"),
                        rs.getString("no_hp"),
                        rs.getString("nominal"),
                        formattedDate
                    });
                }
                Tbl_Keuangan.setModel(model);
            }

        } catch (SQLException e) {
            System.err.println("Gagal ambil data berdasarkan bulan: " + e.getMessage());
            JOptionPane.showMessageDialog(this, "Gagal mengambil data dari database: " + e.getMessage(), "Error Database", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void tampilkanSemuaData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Nama");
        model.addColumn("NIK");
        model.addColumn("No. HP");
        model.addColumn("Nominal");
        model.addColumn("Tanggal");    

        try (Connection conn = sijimpit.Koneksi.getConnection(); Statement stmt = conn.createStatement(); ResultSet rs = stmt.executeQuery("SELECT nama, nik, no_hp, nominal, tanggal FROM menu_pembayaran_warga ORDER BY tanggal ASC")) {

            while (rs.next()) {
                java.sql.Date sqlDate = rs.getDate("tanggal");
                String formattedDate = "";
                if (sqlDate != null) {
                    SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
                    formattedDate = sdf.format(new Date(sqlDate.getTime()));
                }
                model.addRow(new Object[]{
                    rs.getString("nama"),
                    rs.getString("nik"),
                    rs.getString("no_hp"),
                    rs.getString("nominal"),
                    formattedDate
                });
            }
            Tbl_Keuangan.setModel(model);

        } catch (SQLException e) {
            System.err.println("Gagal ambil seluruh data: " + e.getMessage());
            JOptionPane.showMessageDialog(this, "Gagal mengambil seluruh data dari database: " + e.getMessage(), "Error Database", JOptionPane.ERROR_MESSAGE);
        }
    }

    private int ubahBulanKeAngka(String selectedMonth) {
        if (selectedMonth == null) {
            return -1;
        }
        String trimmedSelectedMonth = selectedMonth.trim();
        System.out.println("DEBUG: trimmedSelectedMonth: '" + trimmedSelectedMonth + "'");

        switch (trimmedSelectedMonth) {
            case "Bulan Januari":
                return 1;
            case "Bulan Februari":
                return 2;
            case "Bulan Maret":
                return 3;
            case "Bulan April":
                return 4;
            case "Bulan Mei":
                return 5;
            case "Bulan Juni":
                return 6;
            case "Bulan Juli":
                return 7;
            case "Bulan Agustus":
                return 8;
            case "Bulan September":
                return 9;
            case "Bulan Oktober":
                return 10;
            case "Bulan November":
                return 11;
            case "Bulan Desember":
                return 12;
            default:
                return -1;
        }
    }

